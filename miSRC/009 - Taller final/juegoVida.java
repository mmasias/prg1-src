    static void imprimeLaberinto(){
        int i, j;
        System.out.println("");
        System.out.println("================================================================================");
        for(i=0;i<(elLaberinto.length);i=i+1){
            //System.out.print("|");
            for (j=0;j<(elLaberinto[i].length);j=j+1){

                if((i==posX)&&(j==posY))
                {
                    System.out.print("H");
                }
                else if((posX+alcanceAntorcha>=i)&&(posX-alcanceAntorcha<=i)&&(posY+alcanceAntorcha>=j)&&(posY-alcanceAntorcha<=j))
                {
                         if(elLaberinto[i][j]==0)   { System.out.print(" "); } //  Camino
                    else if(elLaberinto[i][j]==1)   { System.out.print("#"); } //  Pared
                    else if(elLaberinto[i][j]==3)   { System.out.print("~"); } //  Agua
                    else if(elLaberinto[i][j]==4)   { System.out.print("."); } //  Arbustos
                    else if(elLaberinto[i][j]==6)   { System.out.print(":"); } //  Arboleda
                    else if(elLaberinto[i][j]==5)   { System.out.print("^"); } //  Montañas
                    else if(elLaberinto[i][j]==7)   { System.out.print("*"); } //  Riscos
                }
                else
                {
                    System.out.print(" ");
                }
            }
            //System.out.print("|");
            System.out.println();
        }
        System.out.println("================================================================================");
        //System.out.println(" H ["+posX+","+posY+"]");
    }


