    static int nostradamus(int posCeldaX, int posCeldaY){
        int estadoActual;
        int numeroVecinas;
        int estadoFinal=0;

        estadoActual = elLaberinto[posCeldaX][posCeldaY];
        numeroVecinas = elLaberinto[posCeldaX-1][posCeldaY-1]+elLaberinto[posCeldaX][posCeldaY-1]+elLaberinto[posCeldaX+1][posCeldaY-1]+
                        elLaberinto[posCeldaX-1][posCeldaY]                                      +elLaberinto[posCeldaX+1][posCeldaY]  +
                        elLaberinto[posCeldaX-1][posCeldaY+1]+elLaberinto[posCeldaX][posCeldaY+1]+elLaberinto[posCeldaX+1][posCeldaY+1];
        if (estadoActual==1) {
            //  Celda Viva
            if ((numeroVecinas==3)||(numeroVecinas==2)) {estadoFinal=1;} else {estadoFinal=0;}
        } else {
            // Celda Muerta
            if (numeroVecinas==3) {estadoFinal=1;}
        }
        return estadoFinal;
    }

